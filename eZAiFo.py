print('He said his name was %s.' %'Al Ardosa')
# output: He said his name was Al Ardosa.

print('He said his name was %r.' %'Al Ardosa')
# output: He said his name was 'Al Ardosa'.

print('I once caught a fish %s.' %'this \tbig')
# output: I once caught a fish this 	big.

print('I once caught a fish %r.' %'this \tbig')
# output: I once caught a fish 'this \tbig'.

print('I wrote %s programs today.' %4.77)
# output: I wrote 4.77 programs today.

print('I wrote %d programs today.' %4.77)
# output: I wrote 4 programs today.